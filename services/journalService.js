  /**
   * Purchase paid (cash paid)
   * Debit: Accounts Payable
   * Credit: Cash/Bank
   */
  static async createPurchasePaymentEntry(companyId, userId, payment) {
    const lines = [];

    // Determine cash account based on payment method
    let cashAccount;
    const paymentMethod = payment.paymentMethod;
    
    if (paymentMethod === 'bank_transfer' || paymentMethod === 'cheque') {
      // Use the bank account code if provided, otherwise default to cash at bank
      cashAccount = payment.bankAccountCode || DEFAULT_ACCOUNTS.cashAtBank;
    } else if (paymentMethod === 'mobile_money') {
      // Use the momo account code if provided, otherwise default to mtn momo
      cashAccount = payment.bankAccountCode || DEFAULT_ACCOUNTS.mtnMoMo;
    } else if (paymentMethod === 'bank') {
      cashAccount = DEFAULT_ACCOUNTS.cashAtBank;
    } else {
      // cash, credit, or any other method
      cashAccount = DEFAULT_ACCOUNTS.cashInHand;
    }

    // Build description with VAT info if available
    let description = `Payment made for purchase ${payment.purchaseNumber}`;
    if (payment.vatAmount > 0 && payment.netAmount > 0) {
      description += ` (Net: ${payment.netAmount.toLocaleString()}, VAT: ${payment.vatAmount.toLocaleString()})`;
    } else if (payment.vatAmount > 0) {
      description += ` (includes VAT: ${payment.vatAmount.toLocaleString()})`;
    }

    // Debit: Accounts Payable
    lines.push(this.createDebitLine(
      await this.getMappedAccountCode(companyId, 'purchases', 'accountsPayable', DEFAULT_ACCOUNTS.accountsPayable),
      payment.amount,
      description
    ));

    // Credit: Cash/Bank
    lines.push(this.createCreditLine(
      cashAccount,
      payment.amount,
      description
    ));

    return this.createEntry(companyId, userId, {
      date: payment.date || new Date(),
      description,
      sourceType: 'payment',
      sourceReference: payment.purchaseNumber,
      lines,
      isAutoGenerated: true,
      sourceData: {
        vatAmount: payment.vatAmount || 0,
        netAmount: payment.netAmount || payment.amount,
        grossAmount: payment.amount
      }
    });
  }


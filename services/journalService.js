  /**
   * Asset Disposal
   * Entry depends on whether there's gain or loss
   * 
   * If Gain (Proceeds > NBV):
   *   Debit: Cash/Bank (proceeds)
   *   Debit: Accumulated Depreciation (NBV)
   *   Credit: Fixed Asset (cost)
   *   Credit: Gain on Disposal
   * 
   * If Loss (NBV > Proceeds):
   *   Debit: Cash/Bank (proceeds)
   *   Debit: Accumulated Depreciation
   *   Debit: Loss on Disposal
   *   Credit: Fixed Asset (cost)
   */
  static async createAssetDisposalEntry(companyId, userId, asset) {
    const lines = [];

    const cost = asset.purchaseCost || 0;
    const accumulatedDep = asset.accumulatedDepreciation || 0;
    const proceeds = asset.disposalAmount || 0;
    const netBookValue = cost - accumulatedDep;
    const gainLoss = proceeds - netBookValue;

    // Determine cash account
    let cashAccount;
    const paymentMethod = asset.paymentMethod;
    
    if (paymentMethod === 'bank_transfer' || paymentMethod === 'cheque') {
      cashAccount = asset.bankAccountCode || DEFAULT_ACCOUNTS.cashAtBank;
    } else if (paymentMethod === 'mobile_money') {
      cashAccount = asset.bankAccountCode || DEFAULT_ACCOUNTS.mtnMoMo;
    } else if (paymentMethod === 'bank') {
      cashAccount = DEFAULT_ACCOUNTS.cashAtBank;
    } else {
      cashAccount = DEFAULT_ACCOUNTS.cashInHand;
    }

    // Determine asset account
    const assetAccountCode = this.getAssetAccountCode(asset.category);

    // Debit: Cash/Bank (proceeds)
    if (proceeds > 0) {
      lines.push(this.createDebitLine(
        cashAccount,
        proceeds,
        `Asset disposal proceeds: ${asset.name}`
      ));
    }

    // Debit: Accumulated Depreciation
    if (accumulatedDep > 0) {
      lines.push(this.createDebitLine(
        DEFAULT_ACCOUNTS.accumulatedDepreciation,
        accumulatedDep,
        `Remove accumulated depreciation: ${asset.name}`
      ));
    }

    // Credit: Fixed Asset (original cost)
    lines.push(this.createCreditLine(
      assetAccountCode,
      cost,
      `Remove asset cost: ${asset.name}`
    ));

    // Handle Gain or Loss
    if (gainLoss > 0) {
      // Gain
      lines.push(this.createCreditLine(
        DEFAULT_ACCOUNTS.gainOnDisposal,
        gainLoss,
        `Gain on disposal: ${asset.name}`
      ));
    } else if (gainLoss < 0) {
      // Loss
      lines.push(this.createDebitLine(
        DEFAULT_ACCOUNTS.lossOnDisposal,
        Math.abs(gainLoss),
        `Loss on disposal: ${asset.name}`
      ));
    }

    return this.createEntry(companyId, userId, {
      date: asset.disposalDate || new Date(),
      description: `Asset disposed: ${asset.name}`,
      sourceType: 'asset',
      sourceId: asset._id,
      sourceReference: asset.assetCode,
      lines,
      isAutoGenerated: true,
      notes: `Disposal - Cost: ${cost}, Accum Dep: ${accumulatedDep}, Proceeds: ${proceeds}, Gain/Loss: ${gainLoss}`
    });
  }


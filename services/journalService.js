  /**
   * Expense paid immediately
   * Debit: Expense Account (amount excluding VAT)
   * Debit: VAT Receivable / VAT Input (tax amount) — if vatAmount > 0
   * Credit: Cash/Bank (total including VAT)
   */
  static async createExpenseEntry(companyId, userId, expense) {
    const lines = [];
    const totalAmount = expense.amount || 0;
    const vatAmount = expense.vatAmount || 0;
    const whtAmount = expense.withholdingTax || 0;
    
    // Net expense amount (excluding VAT)
    const netAmount = totalAmount - vatAmount;

    // Determine expense account - use provided account code or fall back to category mapping
    let expenseAccount = expense.expenseAccountCode;
    if (!expenseAccount && expense.category) {
      expenseAccount = DEFAULT_ACCOUNTS[expense.category] || DEFAULT_ACCOUNTS.otherExpenses;
    }
    if (!expenseAccount) {
      expenseAccount = DEFAULT_ACCOUNTS.otherExpenses;
    }

    // Determine cash account based on payment method
    let cashAccount;
    const paymentMethod = expense.paymentMethod;
    
    if (paymentMethod === 'bank_transfer' || paymentMethod === 'cheque' || paymentMethod === 'bank' || paymentMethod === 'credit_card') {
      cashAccount = expense.bankAccountCode || DEFAULT_ACCOUNTS.cashAtBank;
    } else if (paymentMethod === 'mobile_money') {
      cashAccount = expense.bankAccountCode || DEFAULT_ACCOUNTS.mtnMoMo || DEFAULT_ACCOUNTS.cashAtBank;
    } else if (paymentMethod === 'cash') {
      cashAccount = DEFAULT_ACCOUNTS.cashInHand;
    } else if (paymentMethod === 'petty_cash') {
      cashAccount = DEFAULT_ACCOUNTS.pettyCash || DEFAULT_ACCOUNTS.cashInHand;
    } else if (paymentMethod === 'payable') {
      cashAccount = DEFAULT_ACCOUNTS.accountsPayable;
    } else {
      cashAccount = DEFAULT_ACCOUNTS.cashInHand;
    }

    // Debit: Expense (net amount excluding VAT)
    if (netAmount > 0) {
      lines.push(this.createDebitLine(
        expenseAccount,
        netAmount,
        expense.description || 'Expense'
      ));
    }

    // Debit: VAT Input (2210) — input tax on expenses
    if (vatAmount > 0) {
      const vatAccount = await this.getMappedAccountCode(companyId, 'tax', 'vatInput', DEFAULT_ACCOUNTS.vatInput);
      lines.push(this.createDebitLine(
        vatAccount,
        vatAmount,
        `${expense.description || 'Expense'} - VAT Input`
      ));
    }

    // Credit: Withholding Tax Payable (2500) — if WHT applies
    if (whtAmount > 0) {
      const whtAccount = await this.getMappedAccountCode(companyId, 'tax', 'withholdingTaxPayable', DEFAULT_ACCOUNTS.withholdingTaxPayable);
      lines.push(this.createCreditLine(
        whtAccount,
        whtAmount,
        `${expense.description || 'Expense'} - WHT withheld`
      ));
    }

    // Credit: Cash/Bank (total amount including VAT, minus WHT withheld)
    const totalCredit = netAmount + vatAmount - whtAmount;
    if (totalCredit > 0) {
      lines.push(this.createCreditLine(
        cashAccount,
        totalCredit,
        expense.description || 'Expense payment'
      ));
    }

    return this.createEntry(companyId, userId, {
      date: expense.date || new Date(),
      description: expense.description || 'Expense payment',
      sourceType: 'expense',
      sourceId: expense._id,
      lines,
      isAutoGenerated: true,
      sourceData: {
        vatAmount,
        netAmount,
        grossAmount: totalAmount,
        taxRate: expense.taxRate || 0,
        taxCode: expense.taxCode || null,
        taxRateId: expense.taxRateId || null
      }
    });
  }


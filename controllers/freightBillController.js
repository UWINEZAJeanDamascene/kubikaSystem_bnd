const FreightBill = require('../models/FreightBill');
const GoodsReceivedNote = require('../models/GoodsReceivedNote');
const JournalService = require('../services/journalService');
const { DEFAULT_ACCOUNTS } = require('../constants/chartOfAccounts');
const transactionService = require('../services/transactionService');

// Helper: resolve credit account for payment method
function resolveCreditAccount(paymentMethod) {
  if (paymentMethod === 'cash') return DEFAULT_ACCOUNTS.cashInHand || '1000';
  if (paymentMethod === 'bank_transfer') return DEFAULT_ACCOUNTS.cashAtBank || '1100';
  if (paymentMethod === 'mobile_money') return DEFAULT_ACCOUNTS.mtnMoMo || '1200';
  return DEFAULT_ACCOUNTS.accountsPayable || '2000';
}

// Create freight bill
exports.createFreightBill = async (req, res, next) => {
  try {
    const companyId = req.user.company._id;
    const payload = req.body;
    payload.company = companyId;
    payload.createdBy = req.user.id;
    payload.status = 'draft';

    const fb = await FreightBill.create(payload);
    res.status(201).json({ success: true, data: fb });
  } catch (err) { next(err); }
};

// Update freight bill (draft only)
exports.updateFreightBill = async (req, res, next) => {
  try {
    const companyId = req.user.company._id;
    const fb = await FreightBill.findOne({ _id: req.params.id, company: companyId });
    if (!fb) return res.status(404).json({ success: false, message: 'Freight bill not found' });
    if (fb.status !== 'draft') return res.status(409).json({ success: false, message: 'Only draft freight bills can be edited' });

    const allowed = ['referenceNo', 'supplier', 'carrierName', 'amount', 'account', 'invoiceDate', 'paymentMethod', 'grnMatches'];
    for (const field of allowed) {
      if (req.body[field] !== undefined) fb[field] = req.body[field];
    }
    await fb.save();
    res.json({ success: true, data: fb });
  } catch (err) { next(err); }
};

// Confirm freight bill -> post journal entry
exports.confirmFreightBill = async (req, res, next) => {
  const companyId = req.user.company._id;

  const runConfirm = async (sess) => {
    const useSession = !!sess;
    const findOpts = useSession ? { session: sess } : {};

    const fb = await FreightBill.findOne({ _id: req.params.id, company: companyId }, null, findOpts);
    if (!fb) throw Object.assign(new Error('Freight bill not found'), { status: 404 });
    if (fb.status === 'confirmed') throw Object.assign(new Error('Freight bill already confirmed'), { status: 400 });

    const freightAcct = fb.account || DEFAULT_ACCOUNTS.freightIn || '5110';
    const creditAcct = resolveCreditAccount(fb.paymentMethod);
    const narration = `Freight Bill ${fb.referenceNo}${fb.carrierName ? ' - ' + fb.carrierName : ''}`;

    const journalLines = [
      JournalService.createDebitLine(freightAcct, fb.amount, narration),
      JournalService.createCreditLine(creditAcct, fb.amount, narration),
    ];

    const created = await JournalService.createEntriesAtomic(
      companyId,
      req.user.id,
      [{
        date: fb.invoiceDate || new Date(),
        description: narration,
        sourceType: 'freight_bill',
        sourceId: fb._id,
        sourceReference: fb.referenceNo,
        lines: journalLines,
        isAutoGenerated: true,
        session: useSession ? sess : null,
      }],
      { session: useSession ? sess : null },
    );
    const je = created && created.length ? created[0] : null;

    fb.status = 'confirmed';
    fb.confirmedBy = req.user.id;
    fb.confirmedAt = new Date();
    fb.journalEntry = je._id;
    await fb.save(useSession ? { session: sess } : {});

    // If GRN matches provided, update GRN freight references (optional linking)
    if (fb.grnMatches && fb.grnMatches.length > 0) {
      for (const match of fb.grnMatches) {
        await GoodsReceivedNote.updateOne(
          { _id: match.grn, company: companyId },
          { $set: { 'freight.actualAmount': match.amountAllocated } },
          { session: sess || null },
        );
      }
    }

    return fb;
  };

  try {
    const result = await transactionService.runInTransaction(async (trx) => await runConfirm(trx));
    res.json({ success: true, data: result });
  } catch (err) {
    if (err && err.status) return res.status(err.status).json({ success: false, message: err.message });
    next(err);
  }
};

// List freight bills
exports.listFreightBills = async (req, res, next) => {
  try {
    const companyId = req.user.company._id;
    const { status, page = 1, limit = 20 } = req.query;
    const query = { company: companyId };
    if (status) query.status = status;

    const skip = (parseInt(page) - 1) * parseInt(limit);
    const bills = await FreightBill.find(query)
      .populate('supplier', 'name code')
      .populate('grnMatches.grn', 'referenceNo')
      .sort({ createdAt: -1 })
      .skip(skip)
      .limit(parseInt(limit))
      .lean();

    const total = await FreightBill.countDocuments(query);
    res.json({ success: true, data: bills, pagination: { page: parseInt(page), limit: parseInt(limit), total } });
  } catch (err) { next(err); }
};


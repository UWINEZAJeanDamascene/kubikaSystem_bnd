// Freight Cost Analysis
exports.getFreightAnalysis = async (req, res, next) => {
  try {
    const companyId = req.user.company._id;
    const { date_from, date_to, supplier_id } = req.query;

    const match = { company: companyId, status: 'confirmed' };
    if (date_from || date_to) {
      match.confirmedAt = {};
      if (date_from) match.confirmedAt.$gte = new Date(date_from);
      if (date_to) match.confirmedAt.$lte = new Date(date_to);
    }
    if (supplier_id) match.supplier = supplier_id;

    // Aggregate freight bills per supplier
    const perSupplier = await FreightBill.aggregate([
      { $match: match },
      { $group: { _id: '$supplier', totalFreight: { $sum: '$amount' }, billCount: { $sum: 1 } } },
      { $lookup: { from: 'suppliers', localField: '_id', foreignField: '_id', as: 'supplierDoc' } },
      { $unwind: { path: '$supplierDoc', preserveNullAndEmptyArrays: true } },
      { $project: { supplierName: { $ifNull: ['$supplierDoc.name', 'Unknown'] }, totalFreight: 1, billCount: 1 } },
    ]);

    // GRN-level analysis
    const grnQuery = { company: companyId, status: 'confirmed' };
    if (date_from || date_to) {
      grnQuery.confirmedAt = {};
      if (date_from) grnQuery.confirmedAt.$gte = new Date(date_from);
      if (date_to) grnQuery.confirmedAt.$lte = new Date(date_to);
    }

    const grns = await GoodsReceivedNote.find(grnQuery)
      .populate('supplier', 'name')
      .populate('purchaseOrder', 'referenceNo')
      .lean();

    const grnAnalysis = grns.map((grn) => {
      const goodsValue = grn.lines.reduce((s, l) => s + (Number(l.qtyReceived) * Number(l.unitCost || 0)), 0);
      const freightAmt = Number(grn.freight?.actualAmount) || 0;
      return {
        grnId: grn._id,
        referenceNo: grn.referenceNo,
        supplierName: grn.supplier?.name || 'N/A',
        poReference: grn.purchaseOrder?.referenceNo || 'N/A',
        goodsValue,
        freightAmount: freightAmt,
        freightPct: goodsValue > 0 ? (freightAmt / goodsValue) * 100 : 0,
        hasFreight: freightAmt > 0,
        receivedDate: grn.receivedDate,
      };
    });

    const totalFreight = perSupplier.reduce((s, r) => s + r.totalFreight, 0);
    const totalGoodsValue = grnAnalysis.reduce((s, r) => s + r.goodsValue, 0);
    const flaggedGRNs = grnAnalysis.filter((g) => !g.hasFreight);

    res.json({
      success: true,
      data: {
        summary: {
          totalFreight,
          totalGoodsValue,
          overallFreightPct: totalGoodsValue > 0 ? (totalFreight / totalGoodsValue) * 100 : 0,
          grnCount: grnAnalysis.length,
          flaggedGRNCount: flaggedGRNs.length,
        },
        perSupplier,
        perGRN: grnAnalysis,
        flaggedGRNs,
      },
    });
  } catch (err) { next(err); }
};

// Delete freight bill (draft only)
exports.deleteFreightBill = async (req, res, next) => {
  try {
    const companyId = req.user.company._id;
    const fb = await FreightBill.findOne({ _id: req.params.id, company: companyId });
    if (!fb) return res.status(404).json({ success: false, message: 'Freight bill not found' });
    if (fb.status !== 'draft') return res.status(409).json({ success: false, message: 'Only draft freight bills can be deleted' });
    await fb.deleteOne();
    res.json({ success: true, message: 'Freight bill deleted' });
  } catch (err) { next(err); }
};
